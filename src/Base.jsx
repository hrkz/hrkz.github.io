import React from 'react'
import { Outlet, Link, useLocation } from 'react-router-dom'

import Home from './Home'

export default function Base() {
  const location = useLocation();

  const [navOpen, setNavOpen] = React.useState(false);
  const [theme, setTheme] = React.useState(localStorage.getItem('theme') || 'light');

  const toggleTheme = () => {
    if (theme === 'light') {
      setTheme('night');
    } else {
      setTheme('light');
    }
  };
  React.useEffect(() => {
    localStorage.setItem('theme', theme);
    document.body.className = theme;
  }, [theme]);

  return (
    <main>
      <header>
        <div className="socials">
          <Link to="/" className="title element">hrkz</Link>
          <a className="element" href="https://github.com/hrkz/">
            <GithubIcon />
          </a>
          <a className="element" href="#">
            <TwitterIcon />
          </a>
          <button
            id="theme-toggle"
            className="element theme-toggle"
            type="button"
            onClick={toggleTheme}
          >
            {theme === 'light' ? <NightIcon /> : <LightIcon />}
          </button>
          <button
            className="element nav-toggle"
            type="button"
            onClick={() => setNavOpen(!navOpen)}
          >
            <div className="line"></div>
            <div className="line"></div>
            <div className="line"></div>
          </button>
        </div>
        <nav className={`${navOpen ? "" : "nav-closed"}`}>
          <ul>
            {[
              ['Notes/blog', '/notes'],
              ['Academic', '/academic'],
              ['Projects', '/projects']
            ].map(([title, url]) => (
              <li key={title} className={`${location.pathname === url ? "current" : ""}`}>
                <Link to={url}>{title}</Link>
              </li>
            ))}
          </ul>
        </nav>
      </header>
      {location.pathname === '/' ? <Home /> : <Outlet />}
    </main>
  )
}

